package pe.com.tss.runakuna.view.model;

public class ReporteMarcacionResultViewModel extends ResultViewModel{
	
	private Long idReporteMarcacion;
	
	private String estado;
	private String  nombreUnidadNegocio;
	private String  nombreDepartamento;
	private String  nombreProyecto;
	private String  jefeInmediato;
	
	public Long getIdReporteMarcacion() {
		return idReporteMarcacion;
	}
	public void setIdReporteMarcacion(Long idReporteMarcacion) {
		this.idReporteMarcacion = idReporteMarcacion;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getNombreUnidadNegocio() {
		return nombreUnidadNegocio;
	}
	public void setNombreUnidadNegocio(String nombreUnidadNegocio) {
		this.nombreUnidadNegocio = nombreUnidadNegocio;
	}
	public String getNombreDepartamento() {
		return nombreDepartamento;
	}
	public void setNombreDepartamento(String nombreDepartamento) {
		this.nombreDepartamento = nombreDepartamento;
	}
	public String getNombreProyecto() {
		return nombreProyecto;
	}
	public void setNombreProyecto(String nombreProyecto) {
		this.nombreProyecto = nombreProyecto;
	}
	public String getJefeInmediato() {
		return jefeInmediato;
	}
	public void setJefeInmediato(String jefeInmediato) {
		this.jefeInmediato = jefeInmediato;
	}
	
	
	
    

		    
    

}
