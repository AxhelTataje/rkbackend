package pe.com.tss.runakuna.view.model;

public class NotificacionTardanzaViewModel {
	
	private Long idEmpleado;
	private String nombreCompleto;
	private String horario;
	private String marcacion;
	private String tardanza;
	private String nombreJefe;		
	private Long idJefe;		
	
	public Long getIdEmpleado() {
		return idEmpleado;
	}	
	public void setIdEmpleado(Long idEmpleado) {
		this.idEmpleado = idEmpleado;
	}
	public String getNombreCompleto() {
		return nombreCompleto;
	}
	public void setNombreCompleto(String nombreCompleto) {
		this.nombreCompleto = nombreCompleto;
	}
	public String getHorario() {
		return horario;
	}
	public void setHorario(String horario) {
		this.horario = horario;
	}
	public String getMarcacion() {
		return marcacion;
	}
	public void setMarcacion(String marcacion) {
		this.marcacion = marcacion;
	}
	public String getTardanza() {
		return tardanza;
	}
	public void setTardanza(String tardanza) {
		this.tardanza = tardanza;
	}
	public String getNombreJefe() {
		return nombreJefe;
	}
	public void setNombreJefe(String nombreJefe) {
		this.nombreJefe = nombreJefe;
	}
	public Long getIdJefe() {
		return idJefe;
	}
	public void setIdJefe(Long idJefe) {
		this.idJefe = idJefe;
	}
	
	
	
	

}
